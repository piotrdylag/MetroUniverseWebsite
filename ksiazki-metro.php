            Powieść postapokaliptyczna autorstwa rosyjskiego pisarza Dmitrija Głuchowskiego. Jest to druga część
            trylogii Metro 2033.
            Akcja powieści rozgrywa się rok po wydarzeniach z pierwszej części.
            Na południu metra stacja Sewastopolska całkowicie polega na dostawach żywności i amunicji z Linii Okrężnej
            (Hanzy), aby skutecznie odpierać ataki mutantów. Bez żadnych wyjaśnień i ostrzeżenia kontakt z Hanzą urywa
            się, a karawany nie docierają do Sewastopolskiej. Grupa zwiadowców wysłana, aby zbadać sytuację, nie wraca.

            Hunter, który zaginął w niewyjaśnionych okolicznościach w pierwszej części trylogii, pracuje jako strażnik
            graniczny na Sewastopolskiej. Zgłasza się na ochotnika, aby nawiązać ponowny kontakt z Linią Okrężną.
            Towarzyszy mu Homer, starzec, były pracownik metra, który szuka inspiracji do swojej książki. Hunter
            kontaktuje się ze strażnikami stacji Tulskiej, ale dochodzi do kłótni między nimi i wrota do stacji zamykają
            się. Myśliwy mówi Homerowi, że stację przejęli bandyci i stacja musi zostać zniszczona. Jednak starzec
            znajduje dziennik jednego ze zwiadowców z poprzedniej ekspedycji i odkrywa, że na stacji wybuchła epidemia
            nieuleczalnej, śmiertelnej choroby.

            Próbując dostać się do Hanzy, muszą przedostać się przez Linię Kachowską, gdzie ratują z ręki bandyty
            nastoletnią dziewczyną, Saszę. Przejeżdżając drezyną przez stację Awtozawodzką Hunter zabija kilku
            strażników, a Sasza powstrzymuje go przed dalszym rozlewem krwi. Podczas ataku mutantów na stację
            Pawielecką, Myśliwy zostaje ciężko ranny, ratując dziewczynę. Sasza wierzy, że może powstrzymać Huntera
            przed dalszym zabijaniem i wraz z Homerem i Leonidem, flecistą, wyruszają na stację Dobryninską, gdzie
            spotykają Huntera.

            Coraz bardziej niestabilny Hunter wyrusza wraz z Homerem do Polis, aby zebrać oddział żołnierzy, którzy
            oczyszczą zakażone stacje. Sasza wyrusza wraz z Leonidem, który twierdzi, że wie, gdzie można znaleźć lek na
            chorobę. Muzyk opowiada dziewczynie, że podobna epidemia wybuchła już kiedyś w metrze, a jedynym lekarstwem
            jest promieniowanie. Zgadza się towarzyszyć jej, aby powstrzymać Huntera przed mordowaniem zarażonych.

            Hunter spotyka się ze swoim starym towarzyszem Młynarzem, dowódcą Zakonu – paramilitarnej jednostki
            broniącej metra. Myśliwy wraz z oddziałem zbrojnych wraca na Dobryninską, a Homer zastanawia się czy
            powstrzymać Huntera przed rozlewem krwi, czy pozwolić mu kontynuować masakrę. Zakon przybywa na Tulską, a
            tam zatrzymuje ich grający Leonid. Sasza próbuje powstrzymać Huntera i wyjaśnia mu, jak wyleczyć zarażonych.
            Wojskowi znajdujący się na stacji detonują bomby, które uszkadzają tunel i dochodzi do zalania stacji.

            Kilka tygodni później Homer i Hunter wracają do służby na stacji granicznej na Sewastopolskiej. Homer szukał
            przez wiele tygodni ciała Saszy, ale nie był w stanie jej odnaleźć. Jej los pozostaje nieznany.
